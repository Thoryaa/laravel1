            <p>
            Job Description and Requirements
            The merchandiser is responsible for ensuring proper visibility / merchandising / displays of products and implementation
            of POS material.
            <br>
            Job Responsibilities:
            <br>
             Monitor stock movement in your outlet to minimize the returns of damaged and / or expired, close to expiry goods
             To inform line manager about any issues / feedback about your products or promotions / activities conducted on your
            products
             To inform line manager about new / related competing product launches, activities / promotions conducted on competing
            products
             Ensuring implementation of display Plano grams
             Inform customers of changes in prices, deals, new product launches etc.
             Support in executing activities like sales promotions, sampling
             Preparing stock reports in outlets in the absence of salesman. If salesman is present, informing him of low stock and
            also keeping the business development manager informed of the same
             Preparation of price surveys, promotion reports
             Preparation of other reports, as per instructions
             If required, taking principals on market visits
            Candidate Requirements:
             Good knowledge & experience in co-operatives, private supermarkets in Kuwait with reputed FMCG food products
             Understanding of merchandising requirements – displays, visibility, stock rotation to maintain stock freshness and
            avoid expiries
             Self-starter
             Self-motivated to achieve monthly KPAs
             Hungry for performance based incentives
             Pleasing personality
             Good relations with trade community – co-operatives and private supermarkets like City Center, Sultan Center etc.
             Minimum 2-3 years’ experience of merchandising in the FMCG (Preferably Food Products) segment in Kuwait with a reputed
            company handling reputed brands
             Own car with valid Kuwait driving licence
             Good knowledge of Arabic – speak and fair knowledge of English
            Education:
             High school diploma
            </p>
